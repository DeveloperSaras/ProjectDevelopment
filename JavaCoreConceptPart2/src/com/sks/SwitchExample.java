package com.sks;

public class SwitchExample {

	public static void main(String[] args) {

		int number = 20;

		switch (number) {
		case 1:
			System.out.println("Value of number is 1");
			break;
		case 2:
			System.out.println("value of number is 2");
			break;
		case 20:
			System.out.println("value of number is 20");
			break;
		case 30:
			System.out.println("value of number is 30");
			break;
		default:
			System.out.println("Value of number is none as expected.");
		}

	}

}
